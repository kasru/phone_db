#ifndef PHONE_DB_H
#define PHONE_DB_H

/**
 * @file phone_db.h
 * @brief База данных телефонных номеров с пакетной загрузкой и инкрементальными обновлениями.
 *
 * Хранит до 300 миллионов записей телефонных номеров.
 * Поддерживает пакетный импорт CSV, инкрементальные операции добавления/удаления/обновления,
 * поиск по ключу через бинарный поиск и экспорт в отсортированный файл.
 */

#include <stdint.h>
#include <stddef.h>

/** @brief Длина ключа телефона в байтах (10 байт = 20 hex-ниблов). */
#define PHONE_KEY_LEN       10

/** @brief Максимальная длина номера телефона в hex-символах (19 цифр). */
#define PHONE_NUM_MAX_HEX   19

/** @brief Магическое число для проверки инициализации структуры.
 *
 * 4 байта: "PDB" + версия базы данных.
 * Например, PDB v1 = 0x50444201 ('P'=0x50, 'D'=0x44, 'B'=0x42, ver=0x01).
 */
#define PHONE_DB_MAGIC      0x50444201

/** @brief Символ-разделитель полей CSV. */
#define CSV_SEP             ';'

/** @brief Символ-разделитель записей CSV. */
#define CSV_RECORD_SEP      '\n'

/** @brief Ёмкость массива записей по умолчанию. */
#define DEFAULT_CAPACITY            1024

/** @brief Ёмкость буфера комментариев по умолчанию (64 КБ). */
#define DEFAULT_COMMENT_BUF_CAP     (64 * 1024)

/** @brief Ёмкость массива отложенных операций по умолчанию. */
#define DEFAULT_PENDING_CAPACITY    256



/**
 * @brief Бинарное представление ключа телефонного номера.
 *
 * Хранит до 19 hex-цифр в 10 байтах, выровненных по правому краю в порядке big-endian.
 * Например, hex-строка "123ABC" хранится как байты [0x00, ..., 0x12, 0x3A, 0xBC].
 * Сравнение — лексикографическое через memcmp.
 */
typedef struct {
    uint8_t bytes[PHONE_KEY_LEN]; /**< Сырые байты ключа, big-endian, выровнены по правому краю. */
} phone_key_t;

/**
 * @brief Запись телефонного номера.
 *
 * Содержит ключ, смещение и длину комментария в буфере, а также срок действия.
 * Используется как в основном отсортированном массиве, так и в отложенных операциях.
 */
typedef struct {
    phone_key_t key;            /**< Ключ телефонного номера. */
    uint32_t comment_offset;    /**< Смещение в буфере комментариев. */
    uint16_t comment_len;       /**< Длина комментария в байтах (макс. 65535). */
    uint32_t expiry;            /**< Дата истечения в днях с эпохи (0 = без срока). */
} phone_record_t;

/**
 * @brief Тип операции для инкрементальных обновлений.
 */
typedef enum {
    OP_ADD    = 'A', /**< Добавить новую запись. */
    OP_DELETE = 'D', /**< Удалить существующую запись. */
    OP_UPDATE = 'U'  /**< Обновить существующую запись. */
} pending_op_t;

/**
 * @brief Отложенная инкрементальная операция.
 *
 * Накапливает инструкции добавления/удаления/обновления перед их применением
 * к основному отсортированному массиву записей.
 */
typedef struct {
    phone_record_t record;      /**< Данные записи (ключ, комментарий, срок действия). */
    uint8_t  op;                /**< Тип операции (OP_ADD, OP_DELETE или OP_UPDATE). */
} pending_entry_t;

/**
 * @brief Основная структура базы данных телефонов.
 *
 * Содержит отсортированный массив записей, буфер комментариев, отложенные
 * инкрементальные операции и их буфер комментариев.
 * Инициализируется через phone_db_init(), освобождается через phone_db_destroy().
 */
typedef struct {
    uint32_t magic;             /**< Магическое число PHONE_DB_MAGIC ("PDB" + версия) для проверки инициализации. */
    phone_record_t *records;    /**< Отсортированный массив телефонных записей. */
    size_t capacity;            /**< Выделенная ёмкость массива records. */
    size_t count;               /**< Текущее количество записей. */

    char *comment_buf;          /**< Буфер для строк комментариев записей. */
    size_t comment_buf_cap;     /**< Выделенная ёмкость comment_buf. */
    size_t comment_buf_size;    /**< Использовано байт в comment_buf. */

    pending_entry_t *pending;   /**< Массив отложенных инкрементальных операций. */
    size_t pending_capacity;    /**< Выделенная ёмкость массива pending. */
    size_t pending_count;       /**< Количество отложенных операций. */
} phone_db_t;

/* -- Операции с ключом телефона ---------------------------------------- */

/**
 * @brief Разобрать hex-строку в ключ телефона.
 *
 * Выравнивает hex-цифры по правому краю 10-байтного ключа.
 * Принимает символы 0-9, A-F, a-f. Пустая строка даёт нулевой ключ.
 *
 * @param[out] key  Ключ для заполнения.
 * @param[in]  hex  Hex-строка (до 19 символов).
 * @return 0 при успехе, -1 если hex содержит недопустимые символы.
 */
int phone_key_from_hex(phone_key_t *key, const char *hex);

/**
 * @brief Преобразовать ключ телефона в 19-символьную hex-строку.
 *
 * Генерирует uppercase hex, дополненный нулями слева.
 *
 * @param[in]  key      Ключ для преобразования.
 * @param[out] buf      Выходной буфер (минимум 20 байт).
 * @param[in]  bufsize  Размер buf в байтах.
 * @return PHONE_NUM_MAX_HEX при успехе, -1 если buf слишком мал.
 */
int phone_key_to_hex(const phone_key_t *key, char *buf, size_t bufsize);

/**
 * @brief Сравнить два ключа телефонов лексикографически.
 *
 * @param[in] a  Первый ключ.
 * @param[in] b  Второй ключ.
 * @return Отрицательное если a < b, 0 если равны, положительное если a > b.
 */
int phone_key_compare(const phone_key_t *a, const phone_key_t *b);

/**
 * @brief Проверить, что ключ телефона состоит из нулей.
 *
 * @param[in] key  Проверяемый ключ.
 * @return 1 если ключ нулевой, 0 в противном случае.
 */
int phone_key_is_empty(const phone_key_t *key);

/* -- Жизненный цикл базы данных ---------------------------------------- */

/**
 * @brief Инициализировать базу данных телефонов.
 *
 * Выделяет начальное хранилище для записей, буфера комментариев
 * и отложенных операций. Все комментарии (загруженные и отложенные)
 * хранятся в одном буфере comment_buf.
 * Обязательно вызвать phone_db_destroy() при завершении.
 *
 * @param[out] db                 База данных для инициализации.
 * @param[in]  capacity           Начальная ёмкость массива записей. Если 0, то DEFAULT_CAPACITY.
 * @param[in]  comment_buf_cap    Начальная ёмкость буфера комментариев. Если 0, то DEFAULT_COMMENT_BUF_CAP.
 * @param[in]  pending_capacity   Начальная ёмкость массива pending. Если 0, то DEFAULT_PENDING_CAPACITY.
 * @return 0 при успехе, -1 при ошибке выделения памяти.
 */
int phone_db_init(phone_db_t *db, size_t capacity, size_t comment_buf_cap,
                  size_t pending_capacity);

/**
 * @brief Освободить все ресурсы базы данных.
 *
 * Безопасен для повторного вызова (guard от двойного освобождения).
 * После вызова все внутренние указатели обнуляются.
 *
 * @param[in,out] db  База данных для уничтожения.
 */
void phone_db_destroy(phone_db_t *db);

/**
 * @brief Сбросить базу данных, пересоздав все структуры.
 *
 * Сохраняет текущие capacity, вызывает destroy, затем init
 * с сохранёнными значениями. Все данные удаляются, но
 * выделенная память перераспределяется с теми же ёмкостями.
 *
 * @param[in,out] db  База данных для сброса.
 * @return 0 при успехе, -1 при ошибке выделения памяти.
 */
int phone_db_reset(phone_db_t *db);

/* -- Загрузка данных --------------------------------------------------- */

/**
 * @brief Загрузить CSV-файл, заменив все существующие данные.
 *
 * При вызове база данных пересоздаётся через phone_db_reset():
 * все текущие данные и отложенные операции удаляются, но
 * выделенная память сохраняется (capacity не меняется).
 *
 * В случае ошибки загрузки база данных остаётся пустой
 * (данные до вызова также удалены).
 *
 * Разбирает строки формата: телефон;комментарий;срок_действия
 * Записи сортируются после загрузки. Дубликаты ключей удаляются.
 *
 * @param[in,out] db       База данных для загрузки.
 * @param[in]     filename Путь к CSV-файлу.
 * @return 0 при успехе, -1 при ошибке файла или разбора.
 */
int phone_db_load_csv(phone_db_t *db, const char *filename);

/**
 * @brief Применить одну инкрементальную операцию (добавление, удаление или обновление).
 *
 * Операции буферизуются в массиве pending. Дублирующие операции
 * для одного ключа дедуплицируются (последняя запись побеждает).
 *
 * @param[in,out] db       База данных для изменения.
 * @param[in]     op       Тип операции: 'A' (добавить), 'D' (удалить) или 'U' (обновить).
 * @param[in]     number   Телефонный номер в hex-формате.
 * @param[in]     comment  Текстовый комментарий (может быть пустым).
 * @param[in]     expiry   Срок действия в формате YYYY-MM-DD (может быть пустым).
 * @return 0 при успехе, -1 при некорректном вводе или ошибке выделения памяти.
 */
int phone_db_apply_increment(phone_db_t *db, char op, const char *number,
                              const char *comment, const char *expiry);

/* -- Поиск ------------------------------------------------------------- */

/**
 * @brief Найти телефонный номер в базе данных.
 *
 * Сначала проверяет отложенные операции (приоритет над основными записями),
 * затем выполняет бинарный поиск по отсортированному массиву.
 *
 * @param[in]  db           База данных для поиска.
 * @param[in]  key          Ключ телефонного номера для поиска.
 * @param[out] comment      Указатель на строку комментария (NULL — игнорировать).
 * @param[out] comment_len  Длина комментария (NULL — игнорировать).
 * @param[out] expiry       Срок действия в днях с эпохи (NULL — игнорировать).
 * @return 0 если найдено, -1 если не найдено или удалено отложенной операцией.
 */
int phone_db_lookup(const phone_db_t *db, const phone_key_t *key,
                    const char **comment, uint16_t *comment_len,
                    uint32_t *expiry);

/* -- Экспорт в файл --------------------------------------------------- */

/**
 * @brief Сохранить базу данных в CSV-файл в отсортированном порядке.
 *
 * Применяет отложенные операции для формирования итогового отсортированного вывода.
 * Формат: телефон;комментарий;срок_действия
 *
 * @param[in]  db       База данных для сохранения.
 * @param[in]  filename Путь к выходному файлу.
 * @return 0 при успехе, -1 при ошибке.
 */
int phone_db_save_sorted(const phone_db_t *db, const char *filename);

/* -- Диагностика ------------------------------------------------------ */

/**
 * @brief Оценить общий объём использования памяти базой данных.
 *
 * Возвращает сумму sizeof(phone_db_t) + выделенные ёмкости
 * (records, буфер комментариев, массив pending).
 *
 * @param[in] db  База данных для измерения.
 * @return Оценка использования памяти в байтах.
 */
size_t phone_db_memory_usage(const phone_db_t *db);

#endif
